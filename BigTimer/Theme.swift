import Foundation
import UIKit

public struct Theme {
    static func lineColor() -> UIColor {
        return UIColor.white
    }
    static func fillColor() -> UIColor {
        let color = UIColor.white
        return color
    }
    static func lineWidth() -> CGFloat {
        return CGFloat(9)
    }
    static func tvLineWidth() -> CGFloat {
        return CGFloat(28)
    }
}
